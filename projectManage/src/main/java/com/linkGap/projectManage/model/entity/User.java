package com.linkGap.projectManage.model.entity;

import java.io.Serializable;

public class User implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1321466301601123776L;

	private Integer id;

	private Integer userId;

	private String userName;

	private String email;

	private Integer age;

	private String password;

	public User() {
		super();
	}

	public User(Integer userId, String userName, String email, Integer age) {
		super();
		this.userId = userId;
		this.userName = userName;
		this.email = email;
		this.age = age;
	}

	public User(Integer id, Integer userId, String userName, String email, Integer age) {
		super();
		this.id = id;
		this.userId = userId;
		this.userName = userName;
		this.email = email;
		this.age = age;
	}

	public User(String userName, Integer age) {
		super();
		this.userName = userName;
		this.age = age;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
